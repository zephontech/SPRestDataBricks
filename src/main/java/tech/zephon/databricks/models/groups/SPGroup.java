package tech.zephon.databricks.models.groups;

/**
 *
 * @author ForesterF
 */
public class SPGroup {
    
    String name;
    String id;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "SPGroup{" + "name=" + name + ", id=" + id + '}';
    }
    
}
